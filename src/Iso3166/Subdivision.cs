        public Subdivision(string code) : this(GetId(code)) { }

        public Subdivision(int subdivisionId)
        {
            if (allRegions.ContainsKey(subdivisionId) == false) throw new ArgumentException("The value isn't a valid subdivision code.", nameof(subdivisionId));

            _id = subdivisionId;
        }

        public string Code => GetEntry(_id).Code;

        public string Name => GetEntry(_id).Name;

        //public string CountryCode => GetEntry(_regionCode).CountryCode;

        //public string RegionCode => GetEntry(_regionCode).RegionCode;

        private static RegionTableEntry GetEntry(int subdivisionId)
        {
            if (allRegions.TryGetValue(subdivisionId, out RegionTableEntry entry) == false)
                throw new ArgumentException($"Unknown subdivision ID {subdivisionId}");

            return entry;
        }

        private static int GetId(string code) => HashCodeUtility.GetPersistentHashCode(code);

        public static ICollection<Subdivision> GetAllRegions() => _regions;

        internal static void Add(Country country, string secondPart, string name)
        {
            var entry = new RegionTableEntry(country.TwoLetterCode, secondPart, name);

            allRegions.Add(entry.Id, entry);

            AddRegionsPerCountry(country, new Subdivision(entry.Id));
        }

        private static void AddRegionsPerCountry(Country country, Subdivision region)
        {
            if (regionsPerCountry.ContainsKey(country) == false)
                regionsPerCountry.Add(country, new List<Subdivision>());

            regionsPerCountry[country].Add(region);
        }

        private static readonly List<Subdivision> NoSubdivisions = new List<Subdivision>();

        public static ICollection<Subdivision> GetCountryRegions(Country country)
        {
            if (regionsPerCountry.ContainsKey(country) == false)
                return NoSubdivisions;

            return regionsPerCountry[country];
        }

        public static implicit operator Subdivision(string value)
        {
            return new Subdivision(value);
        }

        public static bool operator ==(Subdivision left, Subdivision right)
        {
            return left.Equals(right);
        }

        public static bool operator !=(Subdivision left, Subdivision right)
        {
            return left.Equals(right) == false;
        }

        public override bool Equals(object obj)
        {
            if (obj is Subdivision region)
                return Equals(region);

            return false;
        }

        public bool Equals(Subdivision other)
        {
            return _id == other._id;
        }

        public override int GetHashCode()
        {
            unchecked
            {
                return _id ^ 7897;
            }
        }

        public override string ToString() => Code;

